function [p,w] = circleInteg(type)

if type == 1
	p = zeros(5,2); 	w = zeros(5,1);
	p(1,:) = [0,0];		w(1) = 1/2;
	p(2,:) = [1,0];		w(2) = 1/8;
	p(3,:) = [-1,0];	w(3) = 1/8;
	p(4,:) = [0,1];		w(4) = 1/8;
	p(5,:) = [0,-1];	w(5) = 1/8;
elseif type == 2
	p = zeros(4,2); 		w = zeros(4,1);
	p(1,:) = [1/2,1/2];		w(1) = 1/4;
	p(2,:) = [1/2,-1/2];	w(2) = 1/4;
	p(3,:) = [-1/2,1/2];	w(3) = 1/4;
	p(4,:) = [-1/2,-1/2];	w(4) = 1/4;
elseif type == 3
	p = zeros(7,2); 				w = zeros(7,1);
	p(1,:) = [0,0];					w(1) = 1/2;
	p(2,:) = [1,0];					w(2) = 1/12;
	p(3,:) = [-1,0];				w(3) = 1/12;
	p(4,:) = [1/2,1/2*sqrt(3)];		w(4) = 1/12;
	p(5,:) = [1/2,-1/2*sqrt(3)];	w(5) = 1/12;
	p(6,:) = [-1/2,1/2*sqrt(3)];	w(6) = 1/12;
	p(7,:) = [-1/2,-1/2*sqrt(3)];	w(7) = 1/12;
elseif type == 4
	p = zeros(9,2);			w = zeros(9,1);
	p(1,:) = [0,0];			w(1) = 1/6;
	p(2,:) = [1,0];			w(2) = 1/24;
	p(3,:) = [-1,0];		w(3) = 1/24;
	p(4,:) = [0,1];			w(4) = 1/24;
	p(5,:) = [0,-1];		w(5) = 1/24;
	p(6,:) = [1/2,1/2];		w(6) = 1/6;
	p(7,:) = [1/2,-1/2];	w(7) = 1/6;
	p(8,:) = [-1/2,1/2];	w(8) = 1/6;
	p(9,:) = [-1/2,-1/2];	w(9) = 1/6;
elseif type == 5
	p = zeros(7,2); 						w = zeros(7,1);
	p(1,:) = [0,0];							w(1) = 1/4;
	p(2,:) = [sqrt(2/3)*1,0];				w(2) = 1/8;
	p(3,:) = [-sqrt(2/3)*1,0];				w(3) = 1/8;
	p(4,:) = [sqrt(1/6)*1,1/2*sqrt(2)];		w(4) = 1/8;
	p(5,:) = [sqrt(1/6)*1,-1/2*sqrt(2)];	w(5) = 1/8;
	p(6,:) = [-sqrt(1/6)*1,1/2*sqrt(2)];	w(6) = 1/8;
	p(7,:) = [-sqrt(1/6)*1,-1/2*sqrt(2)];	w(7) = 1/8;
elseif type == 6
	p = zeros(21,2) ; w = zeros(21,1);
	p(1,:) = [0,0];							w(1) = 1/9;
	for k=1:10
		p(1+k,1) = sqrt((6-sqrt(6))/10)*1*cos(2*pi*k/10);
		p(1+k,2) = sqrt((6-sqrt(6))/10)*1*sin(2*pi*k/10);
		w(1+k) = (16+sqrt(6))/360;
	end
	for k=1:10
		p(11+k,1) = sqrt((6+sqrt(6))/10)*1*cos(2*pi*k/10);
		p(11+k,2) = sqrt((6+sqrt(6))/10)*1*sin(2*pi*k/10);
		w(11+k) = (16-sqrt(6))/360;
	end
end